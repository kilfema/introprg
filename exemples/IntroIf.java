public class IntroIf {
    public static void main(String[] args){
        boolean faSol = true;
        
        System.out.println("Avui fa sol: " + faSol);
        //si fa sol -> "Qué bonica sa quedao la tarde"
        //no fa sol -> "Qué feo, me quedo en casa con el Netflix"
        
        if (faSol == true) {
            System.out.println("Qué bonica sa quedao la tarde");
        }
    
    
    }
}
